# Asignación múltiple de variables
# Puedes asignar valores a múltiples variables en una sola línea
# Separar los valores con comas
# Ejemplo:

x,y,z = "Manzana", "Banana", "Cereza"
print(x)
print(y)
print(z)

print(x, y, z)

# También puedes asignar el mismo valor a múltiples variables
# Ejemplo:

a = b = c = "Naranja"
print(a)
print(b)
print(c)

print(a, b, c)

# Puedes combinar texto con variables al imprimir
# Ejemplo:

print("Mi fruta favorita es: "+ x)
print(a + " " + z)
print(a + z)

# Asignación múltiple de variables con números
# Ejemplo:

d = 5
e = 6

print(d + e)