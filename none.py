# Non es la ausencia de un valor o un valor nulo en Python.
# Es un objeto especial de su propio tipo de datos llamado 'NoneType'.
x = None
print(x)
print(type(x))

# Estos ejemplos muestran que None es diferente de otros tipos de datos comunes en Python.
print(type("")) 
print(type(0)) 
print(type(False)) 
print(type([])) 