# Las primeras cosas que noto de python son:
# 1. No es necesario declarar el tipo de variable
# 2. Las variables son sensibles a mayúsculas y minúsculas
# 3. Puedo reasignar valores a las variables fácilmente
# 4. Puedo usar diferentes estilos de nomenclatura para las variables
# 5. Hay buenas prácticas recomendadas para nombrar variables
# Ejemplos de cómo definir variables en Python:

X = "Esta es una variable"
print(X)

X = "Ahora esta variable tiene un nuevo valor"
print(X)

x = "Esta es una variable diferente debido a la sensibilidad a mayúsculas y minúsculas"
print(x)

mivariable = "Esta es una variable con un nombre más descriptivo"
print(mivariable)

mi_variable = "Esta es una variable con guion bajo en su nombre"
print(mi_variable)

_mi_variable = "Esta es una variable que comienza con un guion bajo" # Usualmente se usan para indicar que es una variable privada
print(_mi_variable)

miVariable = "Esta es una variable con notación camelCase"
print(miVariable)

MIVARIABLEMAYUSCULA = "Esta es una variable con todas las letras mayúsculas" # Usualmente se usan para constantes
print(MIVARIABLEMAYUSCULA)

miVariable123 = "Esta es una variable que incluye números en su nombre"
print(miVariable123)

