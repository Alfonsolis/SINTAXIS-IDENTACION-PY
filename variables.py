# Diferentes estilos de nomenclatura de variables en Python:

camelCase = "Notación camelCase"
print(camelCase)
PascalCase = "Notación PascalCase"
print(PascalCase)
snake_case = "Notación snake_case"
print(snake_case)

# Buenas prácticas para nombrar variables en Python:
# 1. Usar nombres descriptivos y significativos
# 2. Usar minúsculas y guiones bajos para separar palabras (snake_case)
# 3. Evitar el uso de caracteres especiales y espacios
# 4. No comenzar con números
# 5. Ser consistente con el estilo de nomenclatura elegido