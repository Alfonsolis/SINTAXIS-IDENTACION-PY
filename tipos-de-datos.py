# Tipos de datos: Cadenas de texto
# Diferentes formas de definir cadenas de texto en Python
# Usando comillas simples, dobles y triples
# Ejemplos prácticos:

ComillasSimples = 'Hola, mundo comillas simples'
ComillasDobles = "Hola, mundo comillas dobles"
ComillasTriplesSimples = '''Hola,
mundo comillas triples simples'''

print(ComillasSimples)
print(ComillasDobles)
print(ComillasTriplesSimples)

# Tipos de datos: Números enteros, flotantes y complejos

a = 10          # Número entero
b = 3.14        # Número flotante
c = 2 + 3j      # Número complejo

print(a)
print(b)
print(c)

# Tipos de datos: Listas, tuplas y conjuntos

# Lista: colección ordenada y mutable
mi_lista = [1, 2, 3, 'cuatro', 'cinco']
print(mi_lista)

# Tupla: colección ordenada e inmutable
mi_tupla = (1, 2, 3, 'cuatro', 'cinco')
print(mi_tupla)

# Conjunto: colección no ordenada y mutable. No permite elementos duplicados
mi_conjunto = {1, 2, 3, 'cuatro', 'cinco'}
print(mi_conjunto)

# Tipos de datos: Diccionarios
# Diccionario: colección de pares clave-valor
mi_diccionario = {
    'nombre': 'Juan',
    'edad': 30, 
    'ciudad': 'Madrid'
    }
print(mi_diccionario)

